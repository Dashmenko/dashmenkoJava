public class circleTheSecond {
    private Point center1;
    double radius1;

    public circleTheSecond(Point center1, double radius1) {
        this.center1 = center1;
        this.radius1 = radius1;
    }

    public Point getCenter() {
        return center1;
    }

    public double getRadius() {
        return radius1;
    }

}

